<p>
Data om din brug af websitet, videregives ikke til trediemand.</p>
<p>
Vi benytter herudover en række tredjeparter til opbevaring og behandling af data. Disse behandler udelukkende oplysninger på vores vegne og må ikke anvende dem til egne formål.</p>
<p>
